    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // save student button
        String name = stdNameInput.getText().trim();
        String subject = stdSubjectInput.getText().trim();
        String marksInput = stdMarksInput.getText().trim();

        // validation checks
        if (name.isEmpty() || subject.isEmpty() || marksInput.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "All fields are required!", "Alert", JOptionPane.WARNING_MESSAGE);
            return;
        }

        double marks = Double.parseDouble(marksInput);

        for (Student student : students) {
            if (student.getName().equals(name)) {
                JOptionPane.showMessageDialog(rootPane, "Student Already Exists!", "Alert", JOptionPane.WARNING_MESSAGE);
                return;
            }
        }

        // add the data to array list
        Student student = new Student(name, subject, marks);
        students.add(student);

        // update the table
        loadTable();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // delete student
        int selectedRowIndex = table.getSelectedRow();

        // if user haven't selected a row to delete, show a alert
        if (selectedRowIndex == -1) {
            JOptionPane.showMessageDialog(rootPane, "Please select a row to delete!", "Alert", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        int choice = JOptionPane.showConfirmDialog(rootPane, "Are you sure, you want to delete the student?", "Confirm", JOptionPane.YES_NO_CANCEL_OPTION);

        if (choice == 0) {
            // delete the student
            students.remove(selectedRowIndex);
            JOptionPane.showMessageDialog(rootPane, "Student Deleted!", "Done", JOptionPane.INFORMATION_MESSAGE);
            // cleanup the textboxes
            stdNameInput.setText("");
            stdSubjectInput.setText("");
            stdMarksInput.setText("");
            stdGradeOut.setText("");

            // load the table data
            loadTable();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        // click on a table row.
        // get the selected row
        int selectedRowIndex = table.getSelectedRow();

        // get the student by the index
        Student student = getStudentByIndex(selectedRowIndex);

        stdNameInput.setText(student.getName());
        stdSubjectInput.setText(student.getSubject());
        stdMarksInput.setText(String.valueOf(student.getMarks()));
        stdGradeOut.setText(String.valueOf(getGrade(student.getMarks())));
    }//GEN-LAST:event_tableMouseClicked

