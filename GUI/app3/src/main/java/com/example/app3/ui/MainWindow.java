    private char getGrade(final double marks) {
        if (marks > 100 || marks < 0) {
            JOptionPane.showMessageDialog(rootPane, "Invalid Marks (Should be within 0 -100)!", "Alert", JOptionPane.WARNING_MESSAGE);
            return ' ';
        }
        if (marks >= 75) {
            return 'A';
        } else if (marks >= 65) {
            return 'B';
        } else if (marks >= 55) {
            return 'C';
        } else if (marks >= 35) {
            return 'S';
        } else {
            return 'F';
        }
    }

    private Student getStudentByIndex(int index) {
        if (index == -1) {
            JOptionPane.showMessageDialog(rootPane, "Invalid Index", "Alert", JOptionPane.WARNING_MESSAGE);
            return null;
        }

        // get the student by the index
        Student student = students.get(index);

        return student;
    }

